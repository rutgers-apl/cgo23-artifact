double rlibm_exp2_adapt(float x) {
  float_x fx;
  fx.f = x;
  
  // Take care of special cases
  if ((fx.x & 0x7FFFFFFF) == 0) return 1.0;
  
  if (fx.x <= 876128826) {
    if (fx.x <= 867740218) return 1.0000000298023223876953125;
    return 1.0000000894069671630859375;
  }
  
  if (1124073472 <= fx.x && fx.x <= 3015223867) {
    if (fx.x < 0x80000000) {
      if (fx.x < 0x7F800000) return 3.40282361850336062550457001444955389952e+38;
      if (fx.x == 0x7F800000) return 1.0 / 0.0;
      return 0.0/0.0;
    }
    
    // negative counterpart
    if (fx.x <= 3006835259) return 0.99999998509883880615234375;
    
    return 0.99999995529651641845703125;
  }
  
  if (fx.x >= 3272998913) {
    if (fx.x == 0xFF800000) return 0.0;
    if (fx.x < 0xFF800000) return ldexp(1.0, -151);
    return 0.0/0.0;
  }
  
  // Perform range reduction
  double xp = x * 64;
  int N = (int)xp;
  int N2 = N % 64;
  if (N2 < 0) N2 += 64;
  int N1 = N - N2;
  
  int M = N1 / 64;
  int J = N2;
  double R = x - N * 0.015625;
  
  if (R == 0.0 && N2 == 0) {
    return ldexp(1.0, M);
  }

  double y = 0.0;
  double k = R + 5.7038503905506665603297733468934893608093261718750000000000000000000000e-01;
  double z = k*k;
  
  if (R == -6.4493508489249506965279579162597656250000000000000000000000000000000000e-07) {
    y = 9.9999952316284190789730246251565404236316680908203125000000000000000000e-01;
  } else if (R == 2.9695758130401372909545898437500000000000000000000000000000000000000000e-03) {
    y = 1.0020604729652407005602299250313080847263336181640625000000000000000000e+00;
  } else {
    y = z + 2.8049086757654315249510545982047915458679199218750000000000000000000000e+01;
    y *= z;
    y += 3.4493560079697408582433126866817474365234375000000000000000000000000000e+02;
    y *= (R + 4.8248809193043564746972151624504476785659790039062500000000000000000000e+00);
    y += -9.6996185365055737293005222454667091369628906250000000000000000000000000e+02;
    y *= 1.3534522818023787382041289717449217278044670820236206054687500000000000e-03;
  }

  // Perform output compensation
  return y * ldexp(exp2JBy64[J], M);
}
